import java.util.*;

public class Prac7 {

	public static void main(String[] args) {
		//Even/Odd
		Scanner sc = new Scanner(System.in);
		System.out.println("Enter value of x");
		double x = sc.nextDouble();
		
		if(x%2 == 0) {
			System.out.println("Even");
		}
		else 
		{System.out.println("Odd");
		
		}
	}

}
